package javaprojects.classesAtvd.lista4.q5;

public class Quadrilatero {

    float area, base, altura, lado, perimetro, tlado;

    public float calcularArea(){
        area = base * altura;
        return area;
    }

    public float calcularPerimetro(){
        perimetro = tlado * lado;
        return perimetro;
    }
}
